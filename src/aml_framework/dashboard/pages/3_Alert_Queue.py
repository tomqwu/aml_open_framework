"""Alert Queue -- filterable alert triage view."""

from __future__ import annotations

from datetime import datetime as _dt

import plotly.express as px
import streamlit as st

from aml_framework.dashboard.components import (
    SEVERITY_COLORS,
    chart_layout,
    kpi_card,
    link_to_page,
    page_header,
)
from aml_framework.dashboard.audience import show_audience_context
from aml_framework.engine.constants import Event, Queue


def _bulk_action(case_ids: list[str], event: str, disposition: str) -> None:
    """Apply an action to multiple cases at once."""
    from datetime import datetime, timezone
    from pathlib import Path

    from aml_framework.engine.audit import AuditLedger

    run_dir = Path(st.session_state.run_dir)
    ts = datetime.now(tz=timezone.utc)
    count = 0
    for cid in case_ids:
        AuditLedger.append_to_run_dir(
            run_dir,
            {
                "event": event,
                "case_id": cid,
                "disposition": disposition,
                "source": "bulk_action",
            },
            ts=ts,
        )
        if "df_cases" in st.session_state and not st.session_state.df_cases.empty:
            mask = st.session_state.df_cases["case_id"] == cid
            if mask.any():
                st.session_state.df_cases.loc[mask, "status"] = disposition
        count += 1
    st.success(f"Bulk {event}: {count} cases -> {disposition}")


page_header(
    "Alert Queue",
    "Triage and prioritize alerts generated by the detection engine.",
)
show_audience_context("Alert Queue")

spec = st.session_state.spec
df_alerts = st.session_state.df_alerts

if st.session_state.get("guided_demo"):
    st.info(
        "**Guided Demo -- Alert Queue**\n\n"
        "This is the L1 analyst's work queue. Each alert is generated by "
        "a spec-driven rule. Filter by rule or severity to focus your review."
    )

if df_alerts.empty:
    st.warning("No alerts in this run.")
    st.stop()

# --- Summary KPIs ---
sev_map = {r.id: r.severity for r in spec.rules}
c1, c2, c3, c4 = st.columns(4)
with c1:
    kpi_card("Total Alerts", len(df_alerts), "#2563eb")
with c2:
    high_ct = sum(1 for _, r in df_alerts.iterrows() if sev_map.get(r["rule_id"]) == "high")
    kpi_card("High Severity", high_ct, "#dc2626")
with c3:
    kpi_card("Rules Triggered", df_alerts["rule_id"].nunique(), "#7c3aed")
with c4:
    if "customer_id" in df_alerts.columns:
        kpi_card("Customers", df_alerts["customer_id"].nunique(), "#d97706")
    else:
        kpi_card("Customers", "N/A", "#6b7280")

# Initialize acknowledge/snooze state.
if "acknowledged_alerts" not in st.session_state:
    st.session_state["acknowledged_alerts"] = set()
if "snoozed_alerts" not in st.session_state:
    st.session_state["snoozed_alerts"] = {}  # {alert_key: snooze_until}

st.markdown("<br>", unsafe_allow_html=True)

# --- Filters ---
col_f1, col_f2 = st.columns(2)
with col_f1:
    rule_options = sorted(df_alerts["rule_id"].unique())
    selected_rules = st.multiselect("Filter by rule", rule_options, default=rule_options)
with col_f2:
    severities = sorted(set(sev_map.values()))
    selected_sev = st.multiselect("Filter by severity", severities, default=severities)

filtered = df_alerts[df_alerts["rule_id"].isin(selected_rules)]
filtered = filtered[filtered["rule_id"].map(sev_map).isin(selected_sev)]
display_df = filtered.copy()
display_df["severity"] = display_df["rule_id"].map(sev_map)

# --- Filter out snoozed alerts ---

now = _dt.now()
active_snoozes = {k: v for k, v in st.session_state["snoozed_alerts"].items() if v > now}
st.session_state["snoozed_alerts"] = active_snoozes


# Add status column.
def _alert_key(row):
    return f"{row.get('rule_id', '')}_{row.get('customer_id', '')}"


display_df["_key"] = display_df.apply(_alert_key, axis=1)
display_df["status"] = display_df["_key"].apply(
    lambda k: (
        "snoozed"
        if k in active_snoozes
        else ("reviewed" if k in st.session_state["acknowledged_alerts"] else "new")
    )
)

# Show/hide snoozed toggle.
show_snoozed = st.checkbox("Show snoozed alerts", value=False)
if not show_snoozed:
    display_df = display_df[display_df["status"] != "snoozed"]

# --- Alert Table ---
col_title, col_download = st.columns([3, 1])
col_title.markdown(f"### Alerts ({len(display_df)})")
csv_data = display_df[
    [
        c
        for c in [
            "rule_id",
            "severity",
            "customer_id",
            "sum_amount",
            "count",
            "window_start",
            "window_end",
        ]
        if c in display_df.columns
    ]
].to_csv(index=False)
col_download.download_button(
    "Export CSV",
    csv_data,
    "alerts.csv",
    "text/csv",
    use_container_width=True,
)
show_cols = ["rule_id", "severity", "status"]
for c in ["customer_id", "sum_amount", "count", "window_start", "window_end"]:
    if c in display_df.columns:
        show_cols.append(c)
available = [c for c in show_cols if c in display_df.columns]


def _highlight_severity(val: str) -> str:
    color = SEVERITY_COLORS.get(val, "")
    if color:
        return f"color: {color}; font-weight: 700;"
    return ""


def _status_style(val: str) -> str:
    colors = {"new": "#dc2626", "reviewed": "#059669", "snoozed": "#6b7280"}
    c = colors.get(val, "")
    return f"color: {c}; font-weight: 700;" if c else ""


styled = (
    display_df[available].style.format(na_rep="—").map(_highlight_severity, subset=["severity"])
)
if "status" in available:
    styled = styled.map(_status_style, subset=["status"])
st.dataframe(styled, use_container_width=True, hide_index=True, height=400)

# --- Drill-down to Customer 360 ---
# Streamlit dataframes don't support per-cell click handlers, so the
# drill-down lives below the table as a selectbox + page link. Picking a
# customer_id here writes `selected_customer_id` into session state via
# `link_to_page`; Customer 360 reads it back via `read_param`.
if "customer_id" in display_df.columns and not display_df.empty:
    drill_customers = sorted(display_df["customer_id"].dropna().unique().tolist())
    if drill_customers:
        drill_c1, drill_c2 = st.columns([3, 2])
        with drill_c1:
            drill_cid = st.selectbox(
                "Drill into customer",
                drill_customers,
                key="alertqueue_customer_drill",
            )
        with drill_c2:
            st.write("")  # vertical alignment with selectbox label
            link_to_page(
                "pages/17_Customer_360.py",
                f"→ Open {drill_cid} in Customer 360",
                customer_id=drill_cid,
            )

# --- Acknowledge / Snooze ---
new_alerts = display_df[display_df["status"] == "new"]
if not new_alerts.empty:
    st.markdown("### Quick Actions")
    selected_for_action = st.multiselect(
        "Select alerts to acknowledge or snooze",
        new_alerts["_key"].tolist(),
        format_func=lambda k: k.replace("_", " → ", 1),
    )
    if selected_for_action:
        col_ack, col_snz = st.columns(2)
        with col_ack:
            if st.button(f"Acknowledge ({len(selected_for_action)})", use_container_width=True):
                for k in selected_for_action:
                    st.session_state["acknowledged_alerts"].add(k)
                st.success(f"Acknowledged {len(selected_for_action)} alerts.")
        with col_snz:
            from datetime import timedelta as _td

            if st.button(
                f"Snooze 24h ({len(selected_for_action)})",
                use_container_width=True,
                type="primary",
            ):
                snooze_until = _dt.now() + _td(hours=24)
                for k in selected_for_action:
                    st.session_state["snoozed_alerts"][k] = snooze_until
                st.success(f"Snoozed {len(selected_for_action)} alerts for 24h.")

st.markdown("<br>", unsafe_allow_html=True)

# --- Charts ---
col_left, col_right = st.columns(2)
with col_left:
    st.markdown("### Alert Volume by Rule")
    chart = display_df.groupby("rule_id").size().reset_index(name="count")
    chart["severity"] = chart["rule_id"].map(sev_map)
    chart = chart.sort_values("count", ascending=True)
    fig = px.bar(
        chart,
        y="rule_id",
        x="count",
        color="severity",
        orientation="h",
        color_discrete_map=SEVERITY_COLORS,
        labels={"rule_id": "", "count": "Alerts"},
    )
    fig.update_layout(yaxis_title="", showlegend=False)
    st.plotly_chart(chart_layout(fig, 320), use_container_width=True)

with col_right:
    st.markdown("### Severity Distribution")
    sev_counts = display_df["severity"].value_counts().reset_index()
    sev_counts.columns = ["severity", "count"]
    fig = px.pie(
        sev_counts,
        names="severity",
        values="count",
        color="severity",
        color_discrete_map=SEVERITY_COLORS,
        hole=0.45,
    )
    fig.update_traces(textposition="inside", textinfo="percent+label")
    st.plotly_chart(chart_layout(fig, 320), use_container_width=True)

# --- Case Queue (Analyst Inbox) ---
st.markdown("<br>", unsafe_allow_html=True)
st.markdown("### Case Queue")

df_cases = st.session_state.df_cases
if not df_cases.empty and "status" in df_cases.columns:
    # Group by status.
    status_counts = df_cases["status"].value_counts().reset_index()
    status_counts.columns = ["Status", "Count"]

    status_colors_map = {
        "open": "#2563eb",
        "l1_aml_analyst": "#2563eb",
        "l1_analyst": "#2563eb",
        "l2_investigator": "#7c3aed",
        "str_filing": "#dc2626",
        "sar_filing": "#dc2626",
        "edd_review": "#d97706",
        "closed_no_action": "#6b7280",
    }

    # Show status summary as colored cards.
    status_cols = st.columns(min(len(status_counts), 5))
    for i, row in status_counts.iterrows():
        with status_cols[i % len(status_cols)]:
            color = status_colors_map.get(row["Status"], "#6b7280")
            kpi_card(row["Status"].replace("_", " ").title(), int(row["Count"]), color)

    st.markdown("<br>", unsafe_allow_html=True)

    # Compute SLA status for active cases.
    from aml_framework.generators.sql import parse_window

    queue_sla_map = {q.id: q.sla for q in spec.workflow.queues}
    now = _dt.now()

    # Show open/active cases table with SLA.
    active = df_cases[~df_cases["status"].isin([Queue.CLOSED_NO_ACTION])].copy()
    if not active.empty:

        def _sla_status(row):
            queue = row.get("queue", "")
            sla_str = queue_sla_map.get(queue, "")
            if not sla_str:
                return "N/A"
            try:
                sla_td = parse_window(sla_str)
                sla_hours = sla_td.total_seconds() / 3600
                res_hours = row.get("resolution_hours")
                if res_hours is not None:
                    return "Resolved" if res_hours <= sla_hours else "OVERDUE"
                return f"{sla_str} SLA"
            except Exception:
                return "N/A"

        active["SLA"] = active.apply(_sla_status, axis=1)

        show_cols = ["case_id", "rule_id", "severity", "status", "SLA"]
        if "queue" in active.columns:
            show_cols.insert(3, "queue")
        available = [c for c in show_cols if c in active.columns]

        def _status_color(val):
            c = status_colors_map.get(val, "")
            return f"color: {c}; font-weight: 700;" if c else ""

        def _sla_color(val: str) -> str:
            if val == "OVERDUE":
                return "color: #dc2626; font-weight: 700;"
            if val == "Resolved":
                return "color: #059669; font-weight: 700;"
            return ""

        styled = active[available].style.map(_status_color, subset=["status"])
        if "SLA" in available:
            styled = styled.map(_sla_color, subset=["SLA"])
        st.dataframe(styled, use_container_width=True, hide_index=True)

        # --- Drill-down to Case Investigation ---
        # Same pattern as the Customer 360 drill above — pick a case row
        # and jump to the full investigation view with `case_id` deep link.
        case_drill_options = active["case_id"].tolist()
        if case_drill_options:
            cd_c1, cd_c2 = st.columns([3, 2])
            with cd_c1:
                drill_case_id = st.selectbox(
                    "Drill into case",
                    case_drill_options,
                    key="alertqueue_case_drill",
                )
            with cd_c2:
                st.write("")
                link_to_page(
                    "pages/4_Case_Investigation.py",
                    f"→ Open {drill_case_id} in Case Investigation",
                    case_id=drill_case_id,
                )

        # --- Bulk actions ---
        st.markdown("<br>", unsafe_allow_html=True)
        st.markdown("### Bulk Actions")
        open_cases = active[~active["status"].isin([Queue.STR_FILING, Queue.SAR_FILING])]
        if not open_cases.empty:
            selected = st.multiselect(
                "Select cases",
                open_cases["case_id"].tolist(),
                help="Select cases for bulk action.",
            )
            if selected:
                col_b1, col_b2 = st.columns(2)
                with col_b1:
                    if st.button(
                        f"Bulk Close ({len(selected)})",
                        use_container_width=True,
                    ):
                        _bulk_action(selected, Event.CLOSED, Queue.CLOSED_NO_ACTION)
                with col_b2:
                    if st.button(
                        f"Bulk Escalate ({len(selected)})",
                        use_container_width=True,
                        type="primary",
                    ):
                        _bulk_action(selected, "escalated", "l2_investigator")
    else:
        st.success("All cases resolved.")
else:
    st.caption("No cases in this run.")
